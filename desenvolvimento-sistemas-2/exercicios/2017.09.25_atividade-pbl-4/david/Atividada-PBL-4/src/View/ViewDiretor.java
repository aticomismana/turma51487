    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Diretor dir = new Diretor();
        DAO dao = new DAO();
        
        dir.setNome(nome.getText());
        dir.setCpf(Integer.parseInt(cpf.getText()));
        dir.setSalario(Double.parseDouble(salario.getText()));
        
        dao.createDir(dir);
        tableModel.addRow(dir);
        //readJTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTDiretor.getSelectedRow() !=  -1){
            tableModel.removoRow(jTDiretor.getSelectedRow());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(jTDiretor.getSelectedRow() != - 1){
            tableModel.setValueAt(nome.getText(), jTDiretor.getSelectedRow(), 0);
            tableModel.setValueAt(cpf.getText(), jTDiretor.getSelectedRow(), 1);
            tableModel.setValueAt(salario.getText(), jTDiretor.getSelectedRow(), 2);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

