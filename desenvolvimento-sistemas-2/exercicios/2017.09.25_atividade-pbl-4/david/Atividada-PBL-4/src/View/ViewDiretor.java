    private void jTDiretorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTDiretorKeyReleased
        // TODO add your handling code here:
        if(jTDiretor.getSelectedRow() != -1){
            nome.setText(jTDiretor.getValueAt(jTDiretor.getSelectedRow(), 0).toString());
            cpf.setText(jTDiretor.getValueAt(jTDiretor.getSelectedRow(), 1).toString());
            salario.setText(jTDiretor.getValueAt(jTDiretor.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_jTDiretorKeyReleased

